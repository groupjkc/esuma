    <ul class="expandables" style="margin-top: 47px;">
        <li class="list list1">
            <p class="question">
                <span class="bg-top closed"></span>
                <span class="main-top">Quels genres de projets sont encouragés?</span></p>
            <div class="answer-wrap">

                <ul class="child-expandables">

                    <li class="child-list child1">
                        <p class="child-question"><span class="bg closed-mini"></span><span class="main">Des projets qui favorisent la persévérance scolaire de la petite enfance jusqu’à l’école secondaire.</span></p>
                        <p class="child-answer"><span class="example">Exemples: </span><span class="rest-text">Conception de mécanismes pour offrir du soutien; activités d’une demi-journée au CLSC local; rédaction d’un guide pour aider les parents à comprendre le développement de l’enfant; mise en œuvre d’initiatives communautaires ciblant les jeunes à risque (projets de hip-hop, etc.); soutien individualisé offert aux élèves du secondaire en difficulté; conception de jeux vidéo éducatifs en ligne; aide aux devoirs; production d’une vidéo sur la problématique du décrochage scolaire; aide aux devoirs en ligne.</span></p>
                    </li>
                    <pre></pre>
                    <li class="child-list child2">
                        <p class="child-question"><span class="bg closed-mini"></span><span class="main">Des projets qui aident les parents et favorisent leur engagement à participer à l’éducation de leurs enfants. </span></p>
                        <p class="child-answer"><span class="example">Exemples: </span><span class="rest-text">Formation de partenariats entre les parents et les écoles; cours à l’intention des parents pour leur apprendre à favoriser l’apprentissage de leurs enfants; conférences sur l’aide parentale en compagnie de conférenciers et de modèles à suivre; soutien aux parents qui bénéficient de l’aide sociale et dont les enfants ne sont pas inscrits dans un centre de la petite enfance; encourager les parents à jouer un rôle de soutien pour les devoirs; rédaction d’un guide d’activités à faire avec les enfants à l’intention des parents.</span></p>
                    </li>
                    <pre></pre>
                    <li class="child-list child3">
                        <p class="child-question"><span class="bg closed-mini"></span><span class="main">Des projets qui favorisent des milieux propices à l’apprentissage à la maison, qui répondent à des besoins définis par la communauté ou qui encouragent la persévérance des élèves.</span></p>
                        <p class="child-answer"><span class="example">Exemples: </span><span class="rest-text">Émissions de télévision en inuktitut; programmes éducatifs; soutien aux parents qui bénéficient de l’aide sociale et dont les enfants ne sont pas inscrits dans un centre de la petite enfance; promotion de divers emplois et carrières; production d’une vidéo sur les choix de carrières; production d’une vidéo sur des diplômés et leur cheminement de carrière; production d’annonces pour la radio donnant des renseignements sur des carrières.</span></p>
                    </li>
                    <pre></pre>
                    <li class="child-list child4">
                        <p class="child-question"><span class="bg closed-mini"></span><span class="main">Des projets qui appuient un ou plusieurs facteurs favorisant la persévérance scolaire (familles, facteurs personnels et environnementaux, communauté, éducation de la petite enfance, etc.)</span></p>
                        <p class="child-answer"><span class="example">Exemples: </span><span class="rest-text">Heures de conte dans les centres communautaires; activités d’une demi-journée au CLSC local; activités estivales (camps) à l’intention des jeunes, ciblant la lecture et le calcul; ateliers sur l’alphabétisation et l’aide parentale; rédaction de livres à l’intention des jeunes; enquête sur la participation à l’éducation dans la communauté.</span></p>
                    </li>


                </ul>
            </div>
        </li>

        <li class="list list2">
            <p class="question"><span class="bg-top closed"></span><span class="main-top">Qui peut présenter un projet en vue d’obtenir une aide financière?</span></p>
            <div class="answer-wrap rest-text">
                
                <p>Tout organisme qui a à cœur la persévérance scolaire au Nunavik ou qui œuvre dans ce domaine peut soumettre un projet en vue d’obtenir une aide financière.La collaboration entre les organismes et les particuliers à l’échelle locale est fortement encouragée, afin d’aboutir à une stratégie d’intervention collective.</p>
            </div>
        </li>

        <li class="list list3">
            <p class="question"><span class="bg-top closed"></span><span class="main-top">Quel est le niveau de financement offert?</span></p>
            <div class="answer-wrap rest-text">

                <p>Il est possible d’obtenir une aide financière maximale de 25 000 $. Une aide financière maximale de 100 000 $ peut être versée pour des projets ciblant trois communautés ou plus.(Le financement offert dans le cadre du Programme pour la persévérance scolaire Esuma peut être complémentaire à des fonds obtenus d’autres sources.)</p>
                <p>Les projets peuvent avoir une durée maximale de 52 semaines et doivent être réalisés entre le 1er avril 2014 et le 31 août 2015.</p>
                <p>Les coûts liés aux infrastructures ou aux activités habituelles de fonctionnement d’un organisme ne sont pas admissibles au financement. Les projets existants ne sont pas non plus admissibles au financement.</p>
            </div>
        </li>

        <li class="list list4">
            <p class="question"><span class="bg-top closed"></span><span class="main-top">Quelle est la date limite pour soumettre un projet en vue d’obtenir une aide financière? </span></p>
            <div class="answer-wrap rest-text">

                <p>Le formulaire de demande peut être téléchargé à l’adresse suivante :<a target="_blank" href="http://www.krg.ca/en/employment-a-training/programs" >http://www.krg.ca/en/employment-a-training/ programs</a></p>
                <p>Le formulaire devrait être soumis avant le 28 mars 2014 à l’adresse suivante : <a href="mailto:projects@esuma.ca" target="_top">projects@esuma.ca</a>. Après cette date limite, les demandes seront analysées selon le principe du premier arrivé, premier servi.</p>

            </div>
