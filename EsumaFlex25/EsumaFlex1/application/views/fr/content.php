        <p class="text">Vous souciez-vous du taux d’obtention de diplôme dans votre communauté? Désirez-vous encourager les jeunes à poursuivre leurs études et êtes-vous prêt à apporter votre soutien?
        </p>
        <p class="text">Si vous avez répondu par l’affirmative à ces questions, nous vous invitons à soumettre des projets en vue d’obtenir une aide financière dans le cadre du Programme pour la persévérance scolaire Esuma.
        </p>
